package net.ktds.drink.admin.vo;

public class SoundTrackVO {

	private String soundTrackId;
	private	String FileName;
	private String SoundInfo;
	
	

	public String getSoundTrackId() {
		return soundTrackId;
	}
	public void setSoundTrackId(String soundTrackId) {
		this.soundTrackId = soundTrackId;
	}
	public String getFileName() {
		return FileName;
	}
	public void setFileName(String fileName) {
		FileName = fileName;
	}
	public String getSoundInfo() {
		return SoundInfo;
	}
	public void setSoundInfo(String soundInfo) {
		SoundInfo = soundInfo;
	}
	
}
